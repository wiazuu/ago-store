function makeProduct(i: number, base: Partial<Product>): Product {
  const seed = FOOD_IMAGES[i % FOOD_IMAGES.length];
  const image = img(seed, 800, 800);
  return {
    id: `p${i}`,
    name: base.name || `Refeição ${i}`,
    slug: base.slug || `refeicao-${i}`,
    categoryId: base.categoryId || "c1",
    objectiveIds: base.objectiveIds || ["o1"],
    image,
    gallery: [
      image,
      img(FOOD_IMAGES[(i + 3) % FOOD_IMAGES.length], 800, 800),
      img(FOOD_IMAGES[(i + 5) % FOOD_IMAGES.length], 800, 800),
    ],
    shortDescription: base.shortDescription || "Refeição saudável e balanceada",
    description:
      base.description ||
      "Uma refeição cuidadosamente preparada com ingredientes selecionados, congelada no ponto ideal para preservar sabor e nutrientes. Basta aquecer e servir.",
    price: base.price ?? 32.9,
    promoPrice: base.promoPrice,
    stock: 40 + (i % 20),
    weightG: base.weightG ?? 350,
    calories: base.calories ?? 420,
    protein: base.protein ?? 28,
    carbs: base.carbs ?? 38,
    fats: base.fats ?? 12,
    ingredients:
      base.ingredients ||
      "Frango, arroz integral, brócolis, cenoura, azeite, sal marinho, ervas frescas.",
    allergens: base.allergens || "Pode conter traços de leite e soja.",
    preparation:
      "Retire do freezer, remova a embalagem plástica externa. Micro-ondas: 4 min em potência alta. Forno: 20 min a 180°C.",
    validity: "6 meses no freezer a partir da data de fabricação.",
    storage: "Manter congelado a -18°C. Após aquecido, consumir imediatamente.",
    tags: base.tags || [tagPool[i % tagPool.length], tagPool[(i + 2) % tagPool.length]],
    badge: base.badge ?? (i % 5 === 0 ? "mais-vendido" : i % 7 === 0 ? "novo" : ""),
    active: true,
    featured: i % 3 === 0,
    isNew: i % 6 === 0,
    bestSeller: i % 4 === 0,
    seoTitle: `${base.name} — agô`,
    seoDescription: base.shortDescription,
  };
}

